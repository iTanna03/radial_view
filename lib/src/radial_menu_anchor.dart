/// Represents the anchor point for a radial menu.
enum RadialMenuAnchor {
  /// Top left.
  topLeft,

  /// Top center.
  topCenter,

  /// Top right.
  topRight,

  /// Center left.
  centerLeft,

  /// Center
  center,

  /// Center right.
  centerRight,

  /// Bottom left.
  bottomLeft,

  /// Bottom center.
  bottomCenter,

  /// Bottom right.
  bottomRight,
}
